// reverse(arr, len): reverses the order of the elements
// in array arr.
//
// PRE: arr is not NULL, len is the length of arr
//
// POST: the elements in arr are reversed.  For example, 
// if my array is { 1, 6, 5, 7, 9 }, then after calling 
// reverse on this array, it should be { 9, 7, 5, 6, 1 }.
//
// TIME: O(n), where n is the value of len
void reverse(int arr[], const int len);

